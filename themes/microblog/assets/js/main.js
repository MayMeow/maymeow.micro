// syndication.js
async function loadSyndicationLinks() {
    try {
        const currentUrl = window.location.href;
        
        const response = await fetch('/syndication.json');
        const syndicationData = await response.json();
        
        // Find syndication for current page
        const links = syndicationData[currentUrl];
        
        if (!links || links.length === 0) {
        console.log('No syndication links found for this post');
        return;
        }
        
        // Find or create container
        const container = document.getElementById('syndication-links') 
        || document.querySelector('[data-syndication]');
        
        if (!container) return;
        
        // Create syndication links HTML
        const linksHtml = links.map(link => {
        let displayUrl = link.url;
        
        // Convert Bluesky at:// URLs to http URLs
        if (link.type === 'bsky' && link.url.startsWith('at://')) {
            const match = link.url.match(/at:\/\/(did:[^/]+)\/app\.bsky\.feed\.post\/(\w+)/);
            if (match) {
                displayUrl = `https://bsky.app/profile/${match[1]}/post/${match[2]}`;
            }
        }
        const url = new URL(displayUrl)
        const icon = link.type === 'masto' ? '🐘' : '🦋';
        return `<a href="${displayUrl}" target="_blank" title="${link.type} - ${link.date}">
            ${icon} ${url.hostname}
        </a>`;
        }).join(' ');
        
        container.innerHTML = `<p>Post was syndicated to:</p><div class="syndication-posts">${linksHtml}</div>`;
        
    } catch (error) {
        console.error('Error loading syndication links:', error);
    }
}

// Run when page loads
document.addEventListener('DOMContentLoaded', loadSyndicationLinks);